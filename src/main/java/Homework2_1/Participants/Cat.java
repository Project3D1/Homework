package Homework2_1.Participants;

public class Cat implements Participants {

    private String name;
    private int run;
    private int jump;

    public Cat(String name, int run, int jump) {
        this.name = name;
        this.run = run;
        this.jump = jump;
    }
    public int run() {
        System.out.println("Кот %s бежит %d%n", name, run);
        return run;
    }
    public int jump() {
        System.out.println("Кот %s прыгнул на %d", name, jump);
        return jump;
    }

}




